public class StudentDetails{
String name;
int rollNo;
int mark;

public StudentDetails(String name, int rollNo, int mark) {
this.name = name;
this.rollNo = rollNo;
this.mark = mark;
}

public String toString() {
return "Student1:name=" + name + ",rollnumber=" + rollNo + ",mark=" + mark + "\n";
}
}

